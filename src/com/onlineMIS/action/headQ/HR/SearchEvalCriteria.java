package com.onlineMIS.action.headQ.HR;

public class SearchEvalCriteria {
	private int year;
	private int month;
	private int evaluater;
	private int evaluatee;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getEvaluater() {
		return evaluater;
	}
	public void setEvaluater(int evaluater) {
		this.evaluater = evaluater;
	}
	public int getEvaluatee() {
		return evaluatee;
	}
	public void setEvaluatee(int evaluatee) {
		this.evaluatee = evaluatee;
	}

}
